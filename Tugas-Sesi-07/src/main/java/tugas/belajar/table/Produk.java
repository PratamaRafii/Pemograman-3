package tugas.belajar.table;

public class Produk{
	private String simbol;
	private String name;
	private String price;
	
	public String getSimbol(){
		return this.simbol;
	}
	
	public void setSimbol(String x){
		this.simbol = x;
	}
	
	public String getName(){
		return this.name;
	}
	
	public void setName(String x){
		this.name = x;
	}
	
	public String getPrice(){
		return this.price;
	}
	
	public void setPrice(String x){
		this.price = x;
	}
}